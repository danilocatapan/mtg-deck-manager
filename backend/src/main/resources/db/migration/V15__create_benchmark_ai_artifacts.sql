CREATE TABLE IF NOT EXISTS recommendation_benchmark_ai_jobs (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    status VARCHAR(32) NOT NULL,
    model VARCHAR(64) NOT NULL,
    fixture_version VARCHAR(64) NOT NULL,
    algorithm_version VARCHAR(64) NOT NULL,
    prompt_version VARCHAR(64) NOT NULL,
    total_calls INTEGER NOT NULL,
    completed_calls INTEGER NOT NULL,
    failed_calls INTEGER NOT NULL,
    started_at TIMESTAMP WITH TIME ZONE NOT NULL,
    finished_at TIMESTAMP WITH TIME ZONE,
    error_code VARCHAR(120)
);

CREATE TABLE IF NOT EXISTS recommendation_benchmark_ai_artifacts (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    job_id BIGINT NOT NULL,
    case_id VARCHAR(120) NOT NULL,
    artifact_type VARCHAR(32) NOT NULL,
    input_hash VARCHAR(64) NOT NULL,
    model VARCHAR(64) NOT NULL,
    prompt_version VARCHAR(64) NOT NULL,
    output_json TEXT NOT NULL,
    created_at TIMESTAMP WITH TIME ZONE NOT NULL,
    CONSTRAINT fk_benchmark_ai_artifact_job FOREIGN KEY (job_id) REFERENCES recommendation_benchmark_ai_jobs(id) ON DELETE CASCADE,
    CONSTRAINT uk_benchmark_ai_artifact UNIQUE (job_id, case_id, artifact_type, input_hash)
);

CREATE TABLE IF NOT EXISTS recommendation_benchmark_ai_sets (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    job_id BIGINT NOT NULL,
    status VARCHAR(32) NOT NULL,
    model VARCHAR(64) NOT NULL,
    fixture_version VARCHAR(64) NOT NULL,
    algorithm_version VARCHAR(64) NOT NULL,
    prompt_version VARCHAR(64) NOT NULL,
    total_cases INTEGER NOT NULL,
    promoted_at TIMESTAMP WITH TIME ZONE,
    metrics_json TEXT,
    CONSTRAINT fk_benchmark_ai_set_job FOREIGN KEY (job_id) REFERENCES recommendation_benchmark_ai_jobs(id) ON DELETE CASCADE
);

CREATE INDEX IF NOT EXISTS ix_benchmark_ai_jobs_status_started
    ON recommendation_benchmark_ai_jobs(status, started_at);

CREATE INDEX IF NOT EXISTS ix_benchmark_ai_artifacts_job_case
    ON recommendation_benchmark_ai_artifacts(job_id, case_id);

CREATE INDEX IF NOT EXISTS ix_benchmark_ai_sets_status_promoted
    ON recommendation_benchmark_ai_sets(status, promoted_at);
