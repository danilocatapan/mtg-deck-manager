CREATE TABLE IF NOT EXISTS recommendation_benchmark_runs (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    status VARCHAR(32) NOT NULL,
    fixture_version VARCHAR(64) NOT NULL,
    baseline_version VARCHAR(64) NOT NULL,
    algorithm_version VARCHAR(64) NOT NULL,
    started_at TIMESTAMP WITH TIME ZONE NOT NULL,
    finished_at TIMESTAMP WITH TIME ZONE,
    total_cases INTEGER NOT NULL,
    evaluated_cases INTEGER NOT NULL,
    metrics_json TEXT,
    error_code VARCHAR(120)
);

CREATE TABLE IF NOT EXISTS recommendation_benchmark_case_results (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    run_id BIGINT NOT NULL,
    case_id VARCHAR(120) NOT NULL,
    commander VARCHAR(120) NOT NULL,
    bracket VARCHAR(32) NOT NULL,
    system_output_json TEXT NOT NULL,
    gpt_output_json TEXT NOT NULL,
    option_a_json TEXT NOT NULL,
    option_b_json TEXT NOT NULL,
    system_option VARCHAR(1) NOT NULL,
    metrics_json TEXT NOT NULL,
    CONSTRAINT fk_benchmark_case_run FOREIGN KEY (run_id) REFERENCES recommendation_benchmark_runs(id) ON DELETE CASCADE,
    CONSTRAINT uk_benchmark_case_run_case UNIQUE (run_id, case_id),
    CONSTRAINT ck_benchmark_case_system_option CHECK (system_option IN ('A', 'B'))
);

CREATE TABLE IF NOT EXISTS recommendation_benchmark_reviews (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    run_id BIGINT NOT NULL,
    case_id VARCHAR(120) NOT NULL,
    reviewer_id VARCHAR(255) NOT NULL,
    choice VARCHAR(8) NOT NULL,
    created_at TIMESTAMP WITH TIME ZONE NOT NULL,
    updated_at TIMESTAMP WITH TIME ZONE NOT NULL,
    CONSTRAINT fk_benchmark_review_run FOREIGN KEY (run_id) REFERENCES recommendation_benchmark_runs(id) ON DELETE CASCADE,
    CONSTRAINT uk_benchmark_review_reviewer UNIQUE (run_id, case_id, reviewer_id),
    CONSTRAINT ck_benchmark_review_choice CHECK (choice IN ('A', 'B', 'tie'))
);

CREATE INDEX IF NOT EXISTS ix_benchmark_runs_status_finished
    ON recommendation_benchmark_runs(status, finished_at);

CREATE INDEX IF NOT EXISTS ix_benchmark_cases_run
    ON recommendation_benchmark_case_results(run_id, case_id);

CREATE INDEX IF NOT EXISTS ix_benchmark_reviews_run_case
    ON recommendation_benchmark_reviews(run_id, case_id);
